اتفاقية الشروط والأحكام الخاصة بمقدمي الخدمة

فاهم منصة أنشئت من أجل الوساطة في تقديم الخدمات القانونية، باستخدامك لمنصة فاهم فإنك توافق على هذه الاتفاقية من غير قيود أو شروط، ويحق لمنصة فاهم في أي وقت تعديل هذه الاتفاقية وسيتم نشر النسخة المعدلة على المنصة، وتكون النسخة المعدلة من (اتفاقية الشروط والأحكام) سارية المفعول بعد أسبوع من تاريخ نشرها في المنصة، وبعد النشر يعتبر استمرارك في استخدام المنصة، التزاماً منك بالشروط والبنود الواردة في النسخة المعدلة، ورضاً منك في تطبيق التعديلات.

أولاً: يقصد بالمصطلحات التالية متى ما وردت في هذه الاتفاقية – اتفاقية الشروط والأحكام - ما يلي:

